<strong>Prato-Sarajevo ART INVASION</strong><br> 
<strong>PROGETTO DI RESIDENZA PER GIOVANI ARTISTI ATTIVI IN TOSCANA E BOSNIA</strong><br>
PRATO, APRILE / GIUGNO 2014
<br><br>
Nell'ambito di<br>
